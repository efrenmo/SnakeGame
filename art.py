class Art(Turtle):
    def __init__(self):
        super().__init__()
        self.hideturtle()
        self.penup()
        self.goto(20, -140)
        self.color("white")
        self.intro()

    def intro(self):
        self.write(LOGO, align=ALIGNMENT, font=("Courier", 10, "bold") )

    def clear_intro(self):
        time.sleep(4)
        self.clear()